const express = require('express');
const mainRoutes = require('./routes/mainRoutes');


//////////////////////////////////////////////////////
// CREATE APP
//////////////////////////////////////////////////////
const app = express();

//////////////////////////////////////////////////////
// USES
//////////////////////////////////////////////////////
app.use(express.json());
app.use(express.urlencoded({extended:false}));
app.use("/", mainRoutes);
//////////////////////////////////////////////////////
// SETUP ROUTES
//////////////////////////////////////////////////////
app.get('/homepage', (req, res) => {
    res.send('welcome kenneth')
})

//////////////////////////////////////////////////////
// EXPORT APP
//////////////////////////////////////////////////////
module.exports = app;



